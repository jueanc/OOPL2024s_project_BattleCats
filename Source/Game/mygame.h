#include "nekoButton.h"
#include "nekoAnimation.h"

namespace game_framework {
	/////////////////////////////////////////////////////////////////////////////
	// Constants
	/////////////////////////////////////////////////////////////////////////////

	enum AUDIO_ID {				// 定義各種音效的編號
		AUDIO_DING,				// 0
		AUDIO_LAKE,				// 1
		AUDIO_NTUT,				// 2
		AUDIO_BEGIN,            //遊戲一開始的音效
		AUDIO_GAME              //遊戲進行中的音效
	};

	/////////////////////////////////////////////////////////////////////////////
	// 這個class為遊戲的遊戲開頭畫面物件
	// 每個Member function的Implementation都要弄懂
	/////////////////////////////////////////////////////////////////////////////

	class CGameStateInit : public CGameState {
	public:
		CGameStateInit(CGame *g);
		void OnInit();  								// 遊戲的初值及圖形設定
		void OnBeginState();							// 設定每次重玩所需的變數
		void OnKeyUp(UINT, UINT, UINT); 				// 處理鍵盤Up的動作
		void OnLButtonDown(UINT nFlags, CPoint point);  // 處理滑鼠的動作
	protected:
		void OnShow();									// 顯示這個狀態的遊戲畫面
	private:
		CMovingBitmap logo;								// csie的logo
		void load_background();
		void draw_text();
		CMovingBitmap background;
	};

	/////////////////////////////////////////////////////////////////////////////
	// 這個class為遊戲的遊戲執行物件，主要的遊戲程式都在這裡
	// 每個Member function的Implementation都要弄懂
	/////////////////////////////////////////////////////////////////////////////

	class CGameStateRun : public CGameState {
	public:
		nekoButton		Button;
		CGameStateRun(CGame *g);
		~CGameStateRun();
		void OnBeginState();							// 設定每次重玩所需的變數
		void OnInit();  								// 遊戲的初值及圖形設定
		void OnKeyDown(UINT, UINT, UINT);
		void OnKeyUp(UINT, UINT, UINT);
		void OnLButtonDown(UINT nFlags, CPoint point);  // 處理滑鼠的動作
		void OnLButtonUp(UINT nFlags, CPoint point);	// 處理滑鼠的動作
		void OnMouseMove(UINT nFlags, CPoint point);	// 處理滑鼠的動作 
		void OnRButtonDown(UINT nFlags, CPoint point);  // 處理滑鼠的動作
		void OnRButtonUp(UINT nFlags, CPoint point);	// 處理滑鼠的動作
	protected:
		void OnMove();									// 移動遊戲元素
		void OnShow();									// 顯示這個狀態的遊戲畫面

	private:	
		nekoAnimation	*Neko;		// 貓咪陣列
		//rivalAnimation  *Rival;		// 敵人陣列
		const int		maxNeko;	// 貓咪的最大數量
		//const int		maxRival;	// 敵人的最大數量

		int	activateNeko;				//正在執行onShow，moveForward，loadBitmap的角色數量
		int	currentNekoQuantity;		//目前畫面上的貓咪數量
		int readyToFightNeko;			//即將出動的貓咪(此為Neko陣列中的第N個貓咪)
		int	activateRival;				//正在執行onShow，moveForward，loadBitmap的角色數量
		int	currentRivalQuantity;		//目前畫面上的敵人數量
		int readyToFightRival;			//即將出動的敵人(此為Rival陣列中的第N個敵人)

		int Cat_Counter = 0;
		int Tank_Cat_Counter = 0;

		CMovingBitmap background;

		CMovingBitmap catButton;
		CMovingBitmap Tank_catButton;

		CMovingBitmap Cat[50];
		CMovingBitmap Tank_Cat[50];
		CMovingBitmap Dog[50];
		time_t catButtonTime = time(0);
		time_t Tank_catButtonTime = time(0);
	};

	/////////////////////////////////////////////////////////////////////////////
	// 這個class為遊戲的結束狀態(Game Over)
	// 每個Member function的Implementation都要弄懂
	/////////////////////////////////////////////////////////////////////////////

	class CGameStateOver : public CGameState {
	public:
		CGameStateOver(CGame *g);
		void OnBeginState();							// 設定每次重玩所需的變數
		void OnInit();
	protected:
		void OnMove();									// 移動遊戲元素
		void OnShow();									// 顯示這個狀態的遊戲畫面
	private:
		int counter;	// 倒數之計數器
		void load_background();
	};

}
